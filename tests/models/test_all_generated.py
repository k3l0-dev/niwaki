"""Parametrised tests for all 2222 generated ACI model classes.

Test data is loaded from _test_data.json, which is regenerated by
``uv run python -m niwaki._codegen.generate``.  The JSON is committed so
tests run in CI without ACI schema files.

Fixtures cover:
- no_naming  : classes with no identifying property instantiate with no args
- naming     : naming prop is required; empty / over-long / space-containing
               values are rejected
- rn         : RN string is computed correctly from naming props
- enums      : correct default, invalid value rejected, all valid values accepted
- bools      : correct default value
- surgical   : to_apic() sends only naming props when nothing else is set
"""

from __future__ import annotations

import importlib
import json
from pathlib import Path
from typing import Any

import pytest
from pydantic import ValidationError

# ── Load test fixtures ────────────────────────────────────────────────────────

_DATA_FILE = Path(__file__).parent / "_test_data.json"
_D: dict[str, list] = json.loads(_DATA_FILE.read_text())


def _cls(name: str) -> type:
    """Dynamically import and return a generated class by ACI class name.

    Args:
        name: ACI class name, e.g. ``"fvBD"``.

    Returns:
        The generated Pydantic model class.
    """
    from niwaki.models._generated import _PKG_MAP

    pkg = _PKG_MAP[name]
    return getattr(importlib.import_module(f"niwaki.models._generated.{pkg}.{name}"), name)


# ── 1. Classes with no naming prop: instantiate with no args ──────────────────


@pytest.mark.parametrize("cls_name", _D["no_naming"])
def test_no_naming_instantiates(cls_name: str) -> None:
    """Classes with no identifying property accept zero arguments."""
    _cls(cls_name)()


# ── 2-4. Naming prop constraints ──────────────────────────────────────────────


@pytest.mark.parametrize("cls_name,prop,min_l,max_l,has_pat", _D["naming"])
def test_naming_required(
    cls_name: str,
    prop: str,
    min_l: int,
    max_l: int,
    has_pat: bool,
) -> None:
    """Naming prop is required — instantiating without it raises ValidationError."""
    with pytest.raises(ValidationError):
        _cls(cls_name)()


@pytest.mark.parametrize(
    "cls_name,prop,min_l,max_l,has_pat",
    [t for t in _D["naming"] if t[2] > 0],
)
def test_naming_empty_rejected(
    cls_name: str,
    prop: str,
    min_l: int,
    max_l: int,
    has_pat: bool,
) -> None:
    """Empty string is rejected when naming prop has min_length > 0."""
    with pytest.raises(ValidationError):
        _cls(cls_name)(**{prop: ""})


@pytest.mark.parametrize(
    "cls_name,prop,min_l,max_l,has_pat",
    [t for t in _D["naming"] if t[3] is not None],
)
def test_naming_max_length(
    cls_name: str,
    prop: str,
    min_l: int,
    max_l: int,
    has_pat: bool,
) -> None:
    """Value exceeding max_length raises ValidationError."""
    with pytest.raises(ValidationError):
        _cls(cls_name)(**{prop: "x" * (max_l + 1)})


@pytest.mark.parametrize(
    "cls_name,prop,min_l,max_l,has_pat",
    [t for t in _D["naming"] if t[4]],
)
def test_naming_pattern_rejects_space(
    cls_name: str,
    prop: str,
    min_l: int,
    max_l: int,
    has_pat: bool,
) -> None:
    """Space character is rejected for naming props with a pattern validator."""
    with pytest.raises(ValidationError):
        _cls(cls_name)(**{prop: "a b"})


# ── 5. RN format ──────────────────────────────────────────────────────────────


@pytest.mark.parametrize("cls_name,kwargs,expected_rn", _D["rn"])
def test_rn_format(cls_name: str, kwargs: dict[str, str], expected_rn: str) -> None:
    """RN is computed by substituting naming props into rn_format."""
    assert _cls(cls_name)(**kwargs).rn == expected_rn


# ── 6-8. Enum validation ──────────────────────────────────────────────────────


@pytest.mark.parametrize("cls_name,naming,prop,values,default", _D["enums"])
def test_enum_default(
    cls_name: str,
    naming: dict[str, Any],
    prop: str,
    values: list[str],
    default: str,
) -> None:
    """Enum prop carries the correct default value."""
    assert getattr(_cls(cls_name)(**naming), prop) == default


@pytest.mark.parametrize("cls_name,naming,prop,values,default", _D["enums"])
def test_enum_invalid_rejected(
    cls_name: str,
    naming: dict[str, Any],
    prop: str,
    values: list[str],
    default: str,
) -> None:
    """Invalid enum value raises ValidationError."""
    with pytest.raises(ValidationError):
        _cls(cls_name)(**naming, **{prop: "__invalid__"})


@pytest.mark.parametrize("cls_name,naming,prop,values,default", _D["enums"])
def test_enum_all_valid_accepted(
    cls_name: str,
    naming: dict[str, Any],
    prop: str,
    values: list[str],
    default: str,
) -> None:
    """Every declared enum value is accepted by the model."""
    for v in values:
        _cls(cls_name)(**naming, **{prop: v})


# ── 9. Bool defaults ──────────────────────────────────────────────────────────


@pytest.mark.parametrize("cls_name,naming,prop,expected", _D["bools"])
def test_bool_default(
    cls_name: str,
    naming: dict[str, Any],
    prop: str,
    expected: bool,
) -> None:
    """Bool prop carries the correct default value."""
    assert getattr(_cls(cls_name)(**naming), prop) is expected


# ── 10. Surgical to_apic() ────────────────────────────────────────────────────


@pytest.mark.parametrize("cls_name,naming,expected_attrs", _D["surgical"])
def test_to_apic_surgical(
    cls_name: str,
    naming: dict[str, Any],
    expected_attrs: dict[str, Any],
) -> None:
    """to_apic() sends only naming props when no optional field is explicitly set."""
    obj = _cls(cls_name)(**naming)
    attrs = obj.to_apic()[cls_name]["attributes"]
    assert attrs == expected_attrs
