"""Generate StrEnum classes for all ACI enum field types.

Reads sdk_subset.json (output of data/scripts/03_build_subset.py) and emits:

- ``src/niwaki/models/_generated/enums/_all.py``   — one StrEnum per ACI enum type
- ``data/extracted/enum_mapping.json``              — lookup used by generate.py

Usage (from repo root):
    uv run python -m niwaki._codegen.generate_enums

The generated StrEnum classes:
- Use ``localName`` values as both the member name (UPPER_SNAKE_CASE) and value
  (the string that gets serialised to APIC JSON).
- Provide a ``_missing_`` classmethod that accepts numeric aliases (decimal or
  hex, e.g. ``"0x806"``) and maps them to the canonical ``localName``.
"""

from __future__ import annotations

import json
import re
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any

_REPO_ROOT = Path(__file__).parent.parent.parent.parent
SUBSET_FILE = _REPO_ROOT / "data" / "extracted" / "sdk_subset.json"
MAPPING_FILE = _REPO_ROOT / "data" / "extracted" / "enum_mapping.json"
OUTPUT_FILE = _REPO_ROOT / "src" / "niwaki" / "models" / "_generated" / "enums" / "_all.py"
ENUMS_DIR = OUTPUT_FILE.parent

_HEADER_ALL = """\
# ruff: noqa
# Generated by niwaki generate_enums — do not edit manually.
# Re-generate: uv run python -m niwaki._codegen.generate_enums
from __future__ import annotations

"""

_HEADER_SINGLE = """\
# ruff: noqa
# Generated by niwaki generate_enums — do not edit manually.
from __future__ import annotations

from enum import StrEnum


"""


# ── Naming helpers ─────────────────────────────────────────────────────────────


def _class_name(model_type: str) -> str:
    """Convert an ACI ``modelType`` string to a Python class name.

    Examples::

        "l2:EtherType"      → "L2EtherType"
        "mo:Owner"          → "MoOwner"
        "nw:AdminSt"        → "NwAdminSt"
        "scalar:Enum8"      → "ScalarEnum8"

    Args:
        model_type: ACI model type string, e.g. ``"l2:EtherType"``.

    Returns:
        PascalCase Python class name.
    """
    pkg, _, name = model_type.partition(":")
    return pkg[0].upper() + pkg[1:] + name


def _member_name(local_name: str) -> str:
    """Convert an ACI enum ``localName`` to UPPER_SNAKE_CASE.

    Replaces dashes and spaces with underscores, uppercases the result, and
    prefixes with ``_`` if the name starts with a digit.

    Examples::

        "bd-flood"       → "BD_FLOOD"
        "mac_security"   → "MAC_SECURITY"
        "unspecified"    → "UNSPECIFIED"

    Args:
        local_name: ACI enum localName string.

    Returns:
        Valid UPPER_SNAKE_CASE Python identifier.
    """
    name = re.sub(r"[^a-zA-Z0-9]+", "_", local_name).strip("_").upper()
    if name and name[0].isdigit():
        name = "_" + name
    return name


def _sig_key(model_type: str, values: list[str]) -> str:
    """Stable string key for a ``(model_type, values)`` pair.

    Used as the key in ``enum_mapping.json``.

    Args:
        model_type: ACI model type string.
        values:     Sorted list of canonical enum values.

    Returns:
        A composite key string.
    """
    return model_type + "||" + "|".join(values)


# ── Data collection ────────────────────────────────────────────────────────────


def _collect(subset: dict[str, Any]) -> dict[str, Any]:
    """Gather all unique enum signatures from sdk_subset.json.

    Groups enum fields by ``(model_type, frozenset(values))`` to deduplicate
    across classes.  For ``model_type`` values that have more than one distinct
    set of valid values (rare — only ~6 in the full ACI schema), the class name
    receives a numeric suffix (``MonTargetScope``, ``MonTargetScope2``, …).

    Args:
        subset: Full content of sdk_subset.json.

    Returns:
        Dict mapping ``sig_key → {class_name, values, aliases}`` for every
        unique enum signature.
    """
    # model_type → list of (sig_key, values, aliases)  — in insertion order
    by_model: dict[str, list[tuple[str, list[str], dict[str, str]]]] = defaultdict(list)
    seen_sigs: set[str] = set()
    # sig_key → {value: Cisco comment} — merged across every class sharing the
    # signature (a value may be documented in one class's schema and not another)
    comments_by_sig: dict[str, dict[str, str]] = defaultdict(dict)

    # model_type → the kind that uses it, to prove the invariant below.
    kind_by_model: dict[str, str] = {}

    for cls_data in subset.values():
        for prop in cls_data.get("properties", {}).values():
            kind = prop.get("python_type")
            if kind not in ("enum", "flags"):
                continue
            mt: str = prop.get("model_type", "") or "scalar:Enum8"

            # The extractor already emits a bitmask's members in ascending bit
            # weight — the order the APIC serialises them in.  Preserve it.
            values: list[str] = prop["values"]

            # One model_type, one kind.  Were a type shared between an enum and
            # a bitmask, the two member orders would fork it into two classes
            # and silently renumber every existing enum that follows.
            previous = kind_by_model.setdefault(mt, kind)
            if previous != kind:
                sys.exit(
                    f"generate_enums: modelType {mt!r} is used as both {previous} "
                    f"and {kind} — the member order cannot be both."
                )

            aliases: dict[str, str] = prop.get("aliases", {})
            key = _sig_key(mt, values)
            for val, comment in (prop.get("value_comments") or {}).items():
                comments_by_sig[key].setdefault(val, comment)
            if key not in seen_sigs:
                seen_sigs.add(key)
                by_model[mt].append((key, values, aliases))

    # Assign class names; add numeric suffix for multi-variant model_types
    result: dict[str, Any] = {}
    for mt, entries in sorted(by_model.items()):
        base = _class_name(mt)
        for i, (key, values, aliases) in enumerate(entries):
            cls = base if i == 0 else f"{base}{i + 1}"
            result[key] = {
                "class_name": cls,
                "values": values,
                "aliases": aliases,
                "value_comments": comments_by_sig.get(key, {}),
            }

    return result


# ── Code generation ────────────────────────────────────────────────────────────


def _render_enum(
    class_name: str,
    values: list[str],
    aliases: dict[str, str],
    value_comments: dict[str, str] | None = None,
) -> str:
    """Render a single StrEnum class definition.

    Args:
        class_name: Python class name (PascalCase).
        values:     Sorted list of canonical ``localName`` strings.
        aliases:    Dict mapping numeric aliases to canonical values.
        value_comments: Optional Cisco descriptions per value, emitted as
            member docstrings (picked up by IDE hovers and Sphinx).

    Returns:
        Python source for one StrEnum class.
    """
    lines: list[str] = [f"class {class_name}(StrEnum):\n"]

    for val in values:
        member = _member_name(val)
        lines.append(f'    {member} = "{val}"\n')
        if comment := (value_comments or {}).get(val):
            safe = comment.replace('"""', "'''")
            lines.append(f'    """{safe}"""\n')

    if aliases:
        lines.append("\n    @classmethod\n")
        lines.append(f"    def _missing_(cls, value: object) -> {class_name} | None:\n")
        lines.append('        """Accept numeric aliases used in ACI schema (hex or decimal)."""\n')
        lines.append("        _aliases: dict[str, str] = {\n")
        for alias_val, canonical in sorted(aliases.items()):
            lines.append(f'            "{alias_val}": "{canonical}",\n')
        lines.append("        }\n")
        lines.append(
            "        if isinstance(value, str) and value in _aliases:\n"
            "            return cls(_aliases[value])\n"
            "        return None\n"
        )

    lines.append("\n\n")
    return "".join(lines)


def _render_single_file(
    class_name: str,
    values: list[str],
    aliases: dict[str, str],
    value_comments: dict[str, str] | None = None,
) -> str:
    """Render a standalone .py file for one StrEnum class.

    Args:
        class_name: Python class name (PascalCase).
        values:     Sorted list of canonical ``localName`` strings.
        aliases:    Dict mapping numeric aliases to canonical values.
        value_comments: Optional Cisco descriptions per value.

    Returns:
        Full Python source for ``{class_name}.py``.
    """
    return _HEADER_SINGLE + _render_enum(class_name, values, aliases, value_comments)


def _render_barrel(enum_defs: dict[str, Any]) -> str:
    """Render ``_all.py`` as a re-export barrel for backward compatibility.

    Every enum class is imported from its individual file so that any existing
    code using ``from niwaki.models._generated.enums._all import X`` continues
    to work unchanged.

    Args:
        enum_defs: Output of :func:`_collect`.

    Returns:
        Full Python source for the ``_all.py`` barrel.
    """
    parts: list[str] = [_HEADER_ALL]
    for info in sorted(enum_defs.values(), key=lambda x: x["class_name"]):
        cls = info["class_name"]
        parts.append(f"from niwaki.models._generated.enums.{cls} import {cls}\n")
    return "".join(parts)


# ── Main ───────────────────────────────────────────────────────────────────────


def main() -> None:
    """Read sdk_subset.json and write individual enum files + _all.py barrel + enum_mapping.json.

    Emits one ``{ClassName}.py`` file per StrEnum class under ``enums/``, then
    regenerates ``_all.py`` as a backward-compatible re-export barrel.  Stale
    ``.py`` files from previous runs are removed before writing new ones.

    Raises:
        SystemExit: If ``sdk_subset.json`` is not found.
    """
    if not SUBSET_FILE.exists():
        print(f"ERROR: {SUBSET_FILE} not found — run 03_build_subset.py first", file=sys.stderr)
        sys.exit(1)

    subset: dict[str, Any] = json.loads(SUBSET_FILE.read_text())
    enum_defs = _collect(subset)

    ENUMS_DIR.mkdir(parents=True, exist_ok=True)

    # Remove stale enum files so renamed / removed enums don't accumulate.
    for stale in ENUMS_DIR.glob("*.py"):
        if stale.name not in ("__init__.py",):
            stale.unlink()

    # Emit one file per StrEnum class.
    for info in enum_defs.values():
        cls = info["class_name"]
        content = _render_single_file(
            cls, info["values"], info["aliases"], info.get("value_comments")
        )
        (ENUMS_DIR / f"{cls}.py").write_text(content)

    # Emit _all.py as a backward-compat barrel.
    OUTPUT_FILE.write_text(_render_barrel(enum_defs))

    mapping = {key: info["class_name"] for key, info in enum_defs.items()}
    MAPPING_FILE.write_text(json.dumps(mapping, indent=2, sort_keys=True))

    print(f"Generated {len(enum_defs)} individual StrEnum files  →  {ENUMS_DIR}")
    print("  + _all.py barrel (backward-compat re-exports)")
    print(f"Mapping written  →  {MAPPING_FILE}")


if __name__ == "__main__":
    main()
