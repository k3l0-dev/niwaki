"""Generate src/niwaki/domain/_child_map.py from ACI schemas + vocabulary.yaml.

The child map is a dict[str, dict[str, str]] keyed by parent ACI class name.
The nested dict maps method name → child ACI class name.  The special key
"_root" covers classes directly under ``uni`` (pol:Uni).

Two additional dicts are emitted:

- ``RS_TARGET_PROP`` — ``{rs_class: tn_prop_name}`` for Rs singleton classes
  that have exactly one configurable ``tn*`` prop.  Used by
  ``NiwakiNode.__getattr__`` to accept an optional positional ``target_name``
  argument.

- ``REFERENCE_MAP`` / ``TARGET_SUBCLASSES`` — the design resolver's relation
  tables: ``{owner: {target: (rs_class, flavor)}}`` with abstract targets
  expanded to their generated concrete subclasses.

Usage:
    uv run python -m niwaki._codegen.generate_domain
"""

from __future__ import annotations

import json
import re
from collections import defaultdict
from pathlib import Path
from typing import Any

import yaml

SCHEMA_DIR = Path("data/schemas/mo-apic-v6.0_9c")
VOCABULARY_FILE = Path("src/niwaki/domain/vocabulary.yaml")
OUT_FILE = Path("src/niwaki/domain/_child_map.py")

HEADER = """\
# ruff: noqa
# Generated by niwaki generate_domain — do not edit manually.
# Re-generate: uv run python -m niwaki._codegen.generate_domain
from __future__ import annotations

from typing import Literal

"""


def _normalise(dotted: str) -> str:
    """'fv:Tenant' → 'fvTenant', 'pol:Uni' → 'polUni'."""
    if ":" not in dotted:
        return dotted
    pkg, name = dotted.split(":", 1)
    return pkg + name


_EXCLUDED_MO_CATEGORIES: frozenset[str] = frozenset(
    {
        # Reverse relations (Rt*): auto-maintained by APIC, never user-created.
        "RelationshipFromLocal",
        # Fault instances: operational state, not configuration objects.
        "FaultCurrent",
    }
)


def _load_schemas() -> dict[str, dict[str, Any]]:
    """Load all configurable non-abstract ACI classes from the schema dir."""
    classes: dict[str, dict[str, Any]] = {}
    for f in SCHEMA_DIR.glob("*.json"):
        raw = json.loads(f.read_text())
        for _key, entry in raw.items():
            mo_category: str = entry.get("moCategory", "")
            if not (
                entry.get("isConfigurable")
                and not entry.get("isAbstract")
                and not entry.get("isReadOnly", False)
                and mo_category not in _EXCLUDED_MO_CATEGORIES
            ):
                continue

            identified_by: list[str] = list(entry.get("identifiedBy", []))
            is_rs = mo_category == "RelationshipToLocal"
            is_rs_singleton = is_rs and not identified_by

            # For Rs singleton classes, find the single configurable tn* prop.
            props_data: dict[str, Any] = entry.get("properties", {})
            tn_props = [
                p
                for p, v in props_data.items()
                if p.startswith("tn") and not v.get("isReadOnly") and not v.get("isImplicit")
            ]
            rs_target_prop = tn_props[0] if is_rs_singleton and len(tn_props) == 1 else ""

            # Reference flavor (Rs classes only): a configurable tn* prop means
            # the relation targets by *name*; otherwise a configurable tDn
            # means it targets by *DN*; neither → not resolvable, excluded
            # from REFERENCE_MAP.
            flavor = ""
            if is_rs:
                if len(tn_props) == 1:
                    flavor = "name"
                elif props_data.get("tDn", {}).get("isConfigurable"):
                    flavor = "dn"

            # Normalised target class from relation metadata, falling back to
            # the tCl default (already a concrete class name).
            to_mo_raw: str = entry.get("relationInfo", {}).get("toMo", "")
            to_mo = _normalise(to_mo_raw) if to_mo_raw else ""
            if is_rs and not to_mo:
                to_mo = props_data.get("tCl", {}).get("default", "")

            classes[f.stem] = {
                "label": entry.get("label", ""),
                "className": entry.get("className", ""),
                "classPkg": entry.get("classPkg", ""),
                "containedBy": [_normalise(cb) for cb in entry.get("containedBy", {})],
                "identifiedBy": identified_by,
                "rs_target_prop": rs_target_prop,
                "to_mo": to_mo,
                "flavor": flavor,
                "super_classes": [_normalise(sc) for sc in entry.get("superClasses", [])],
            }
    return classes


_RESERVED_METHODS: frozenset[str] = frozenset(
    {
        "type",
        "class",
        "import",
        "pass",
        "return",
        "any",
        "all",
        "id",
        "in",
        "is",
        "or",
        "and",
        "not",
        "del",
        "if",
    }
)

_DIRECTION_SUFFIXES: dict[str, str] = {
    "Prov": "provider",
    "Cons": "consumer",
    "Provider": "provider",
    "Consumer": "consumer",
    "In": "in",
    "Out": "out",
}

# Only drop structural/shape words that add no meaning when siblings share them.
# "policy" is intentionally excluded: bfd_interface_policy must not collapse
# into bfd_interface (a BFD *instance* accessor) under the same parent.
_GENERIC_LABEL_WORDS: frozenset[str] = frozenset(
    {"profile", "rule", "configuration", "definition", "instance"}
)


def _normalise_method(name: str) -> str:
    """Append trailing underscore if name is a Python reserved word."""
    return name + "_" if name in _RESERVED_METHODS else name


def _label_key(label: str) -> str:
    """'Interface Profile' → 'interface_profile'."""
    return re.sub(r"[^a-z0-9]+", "_", label.lower()).strip("_")


def _short_label_key(label: str) -> str:
    """Drop generic tail words: 'Interface Profile' → 'interface'."""
    words = [w for w in _label_key(label).split("_") if w not in _GENERIC_LABEL_WORDS]
    return "_".join(words) if words else _label_key(label)


def _derive_name(aci_class: str, label: str, class_name: str) -> str:
    """Derive a snake_case method name from label or class name."""
    if label:
        return _label_key(label)
    if class_name:
        s = re.sub(r"(?<=[a-z0-9])(?=[A-Z])", "_", class_name)
        return s.lower()
    stripped = re.sub(r"^(fv|vz|l3ext|l2ext|infra|phys|fabric|pim|bgp|ospf)", "", aci_class)
    s = re.sub(r"(?<=[a-z0-9])(?=[A-Z])", "_", stripped or aci_class)
    return s.lower()


def _longest_common_suffix(strings: list[str]) -> str:
    """Return the longest string that is a suffix of every element in strings.

    Examples:
        ['MhIfP', 'IfP']  → 'IfP'
        ['RsAnyToProv', 'RsAnyToCons'] → 's'  (not useful, handled by strategy A)
    """
    if not strings or any(not s for s in strings):
        return ""
    rev = [s[::-1] for s in strings]
    suffix: list[str] = []
    for chars in zip(*rev, strict=False):
        if len(set(chars)) == 1:
            suffix.append(chars[0])
        else:
            break
    return "".join(reversed(suffix))


def _resolve_group(
    group: list[str],
    classes: dict[str, dict[str, Any]],
    shared_label: str,
) -> dict[str, str]:
    """Assign unique method names to classes that share the same label-derived name.

    Three-strategy cascade applied in order:

    - **A — direction suffix**: ``className`` ends with ``Prov``/``Cons``/… →
      ``{label_key}_{direction}`` (e.g. ``contract_provider``).
    - **B — classPkg discriminant**: ``classPkg`` varies across the remaining
      colliders → ``{pkg}_{short_label}`` (e.g. ``eigrp_interface``).
    - **C — intra-pkg className prefix**: same pkg, multiple classes → extract
      the unique ``className`` prefix relative to the longest common suffix
      → ``{pkg}_{prefix_snake}_{short_label}`` (e.g. ``pim_ipv6_interface``).

    Args:
        group: ACI class names that all derived the same method name.
        classes: Full class info dict from :func:`_load_schemas`.
        shared_label: The label string that caused the collision.

    Returns:
        ``{aci_class: unique_method_name}`` for every class in ``group``.
    """
    result: dict[str, str] = {}
    lk = _label_key(shared_label)
    slk = _short_label_key(shared_label)

    # Strategy A: className direction suffix ──────────────────────────────────
    remaining: list[str] = []
    for cls in group:
        cn = classes[cls]["className"]
        resolved: str | None = None
        for sfx, readable in _DIRECTION_SUFFIXES.items():
            if cn.endswith(sfx):
                resolved = f"{lk}_{readable}"
                break
        if resolved:
            result[cls] = resolved
        else:
            remaining.append(cls)

    if not remaining:
        return result

    # Strategy B: group remaining by classPkg ─────────────────────────────────
    pkg_groups: dict[str, list[str]] = defaultdict(list)
    for cls in remaining:
        pkg_groups[classes[cls]["classPkg"]].append(cls)

    for pkg, pkg_group in pkg_groups.items():
        # When the label already starts with the pkg name (e.g. "BFD Interface Policy"
        # under classPkg "bfd"), strip the redundant prefix so we don't produce
        # "bfd_bfd_interface_policy".
        pkg_prefix = pkg + "_"
        eff_slk = slk[len(pkg_prefix) :] if slk.startswith(pkg_prefix) else slk

        if len(pkg_group) == 1:
            result[pkg_group[0]] = f"{pkg}_{eff_slk}"
        else:
            # Strategy C: same pkg → find unique className prefix ─────────────
            cns = [classes[c]["className"] for c in pkg_group]
            common_sfx = _longest_common_suffix(cns)
            for cls in pkg_group:
                cn = classes[cls]["className"]
                # When cn equals the common suffix there is no distinguishing prefix.
                unique = cn[: -len(common_sfx)] if common_sfx and cn != common_sfx else ""
                if unique:
                    u_snake = re.sub(r"(?<=[a-z0-9])(?=[A-Z])", "_", unique).lower()
                    result[cls] = f"{pkg}_{u_snake}_{eff_slk}" if eff_slk else f"{pkg}_{u_snake}"
                else:
                    result[cls] = f"{pkg}_{eff_slk}"

    return result


def _build_child_map(
    classes: dict[str, dict[str, Any]],
    jargon: dict[str, str],
) -> tuple[dict[str, dict[str, str]], int]:
    """Build ``{parent_class_name: {method_name: child_class_name}}``.

    Two-pass algorithm:

    1. **Collect**: for every child class, compute its candidate method name
       (jargon override or label-derived) and accumulate all candidates per
       ``(parent, method)`` slot.
    2. **Resolve**: slots with a single candidate are written as-is; slots
       with multiple candidates apply :func:`_resolve_group` — explicit jargon
       entries keep their slot, implicit colliders receive disambiguated names.

    Returns:
        ``(child_map, collisions_resolved)``
    """
    # Pass 1 ── accumulate (parent, method) → [(child_cls, is_explicit), …]
    slots: dict[str, dict[str, list[tuple[str, bool]]]] = defaultdict(lambda: defaultdict(list))

    for child_cls, info in classes.items():
        is_explicit = child_cls in jargon
        method = _normalise_method(
            jargon[child_cls]
            if is_explicit
            else _derive_name(child_cls, info["label"], info["className"])
        )
        for parent_raw in info["containedBy"]:
            parent = "_root" if parent_raw == "polUni" else parent_raw
            slots[parent][method].append((child_cls, is_explicit))

    # Pass 2 ── build child_map, resolving collisions
    child_map: dict[str, dict[str, str]] = {}
    collisions_resolved = 0

    for parent, method_slots in slots.items():
        child_map[parent] = {}

        for method, candidates in method_slots.items():
            if len(candidates) == 1:
                child_map[parent][method] = candidates[0][0]
                continue

            collisions_resolved += 1
            explicit = [cls for cls, is_exp in candidates if is_exp]
            implicit = [cls for cls, is_exp in candidates if not is_exp]

            if explicit:
                # Jargon wins its slot; implicit colliders get new names.
                child_map[parent][method] = explicit[0]
                if implicit:
                    shared_label = classes[implicit[0]]["label"]
                    for cls, new_method in _resolve_group(implicit, classes, shared_label).items():
                        new_method = _normalise_method(new_method)
                        if new_method not in child_map[parent]:
                            child_map[parent][new_method] = cls
            else:
                # All label-derived → resolve the whole group.
                all_cls = [cls for cls, _ in candidates]
                shared_label = classes[all_cls[0]]["label"]
                for cls, new_method in _resolve_group(all_cls, classes, shared_label).items():
                    new_method = _normalise_method(new_method)
                    if new_method not in child_map[parent]:
                        child_map[parent][new_method] = cls

    return child_map, collisions_resolved


def _build_rs_target_prop(classes: dict[str, dict[str, Any]]) -> dict[str, str]:
    """Build RS_TARGET_PROP from schema data.

    Returns:
        ``{rs_class: tn_prop_name}`` for Rs singletons with exactly one
        configurable ``tn*`` prop — the facade's read-side Rs navigation
        accepts an optional positional target name thanks to this table.
    """
    return {
        cls_name: info["rs_target_prop"]
        for cls_name, info in classes.items()
        if info["rs_target_prop"]
    }


def _build_reference_map(
    classes: dict[str, dict[str, Any]],
) -> tuple[dict[str, dict[str, tuple[str, str]]], dict[str, list[str]], dict[str, int]]:
    """Build REFERENCE_MAP and TARGET_SUBCLASSES from schema data.

    REFERENCE_MAP is the design resolver's relation table: every resolvable
    relation carries its flavor (``"name"`` for ``tn*`` props, ``"dn"``
    for configurable ``tDn``), and **abstract targets are expanded**
    to every generated concrete subclass (``infra:DomP`` → ``physDomP``,
    ``l3extDomP``, …) so a design can bind by the class the user actually
    declared.  The abstract name itself stays in the map alongside its
    expansions (curated aliases may point at the abstract class).

    The ambiguity rule is re-applied *after* expansion: an ``(owner, target)``
    pair reached by two different Rs classes is dropped from the map.

    Returns:
        ``(reference_map, target_subclasses, stats)`` where *reference_map* is
        ``{owner: {target: (rs_class, flavor)}}``, *target_subclasses* maps
        each referenced abstract class to its generated concrete subclasses,
        and *stats* counts exclusions for the generation report.
    """
    # Reverse superClasses index: ancestor → generated concrete subclasses.
    subclasses: dict[str, list[str]] = defaultdict(list)
    for cls_name, info in classes.items():
        for ancestor in info["super_classes"]:
            subclasses[ancestor].append(cls_name)

    stats = {"no_target": 0, "no_flavor": 0, "ambiguous": 0}
    candidates: dict[tuple[str, str], set[tuple[str, str]]] = defaultdict(set)
    referenced_abstracts: set[str] = set()

    for cls_name, info in classes.items():
        if not info["flavor"] and info["to_mo"]:
            stats["no_flavor"] += 1
        if not info["flavor"]:
            continue
        to_mo = info["to_mo"]
        if not to_mo:
            stats["no_target"] += 1
            continue

        targets = [to_mo]
        if to_mo not in classes:  # abstract → expand to concrete subclasses
            expanded = subclasses.get(to_mo, [])
            if expanded:
                referenced_abstracts.add(to_mo)
                targets.extend(expanded)
        for owner in info["containedBy"]:
            for target in targets:
                candidates[(owner, target)].add((cls_name, info["flavor"]))

    reference_map: dict[str, dict[str, tuple[str, str]]] = {}
    for (owner, target), entries in sorted(candidates.items()):
        if len(entries) != 1:
            stats["ambiguous"] += 1
            continue
        reference_map.setdefault(owner, {})[target] = next(iter(entries))

    target_subclasses = {
        abstract: sorted(subclasses[abstract]) for abstract in sorted(referenced_abstracts)
    }
    return reference_map, target_subclasses, stats


def _render(
    child_map: dict[str, dict[str, str]],
    rs_target_prop: dict[str, str],
    reference_map: dict[str, dict[str, tuple[str, str]]],
    target_subclasses: dict[str, list[str]],
    class_pkg: dict[str, str],
) -> str:
    """Render the Python source for _child_map.py."""
    lines: list[str] = [HEADER]

    # ── CHILD_MAP ─────────────────────────────────────────────────────────────
    lines.append(
        "# Maps parent ACI class name → {method_name → child ACI class name}.\n"
        '# The special key "_root" covers objects directly under uni (pol:Uni).\n'
        "CHILD_MAP: dict[str, dict[str, str]] = {\n"
    )
    for parent in sorted(child_map):
        children = child_map[parent]
        if not children:
            continue
        lines.append(f"    {parent!r}: {{\n")
        for method in sorted(children):
            lines.append(f"        {method!r}: {children[method]!r},\n")
        lines.append("    },\n")
    lines.append("}\n\n")

    # ── RS_TARGET_PROP ────────────────────────────────────────────────────────
    lines.append(
        "# Rs singleton classes that have exactly one configurable tn* prop.\n"
        "# NiwakiNode.__getattr__ uses this for read-side Rs navigation:\n"
        "# epg.custom_qos_policy().read()\n"
        "RS_TARGET_PROP: dict[str, str] = {\n"
    )
    for cls_name in sorted(rs_target_prop):
        lines.append(f"    {cls_name!r}: {rs_target_prop[cls_name]!r},\n")
    lines.append("}\n\n")

    # ── REFERENCE_MAP ─────────────────────────────────────────────────────────
    lines.append(
        "# Unambiguous (owner_class, target_class) → (Rs class, flavor) pairs.\n"
        '# Flavor "name" → the Rs targets by tn* prop (rs_cls(name=...));\n'
        '# flavor "dn" → the Rs targets by DN (rs_cls(target_dn=...)).\n'
        "# Abstract targets are expanded to their generated concrete subclasses\n"
        "# and also kept under their own name (for curated abstract aliases).\n"
        "# The design resolver (niwaki.design._resolver) resolves through this.\n"
        'REFERENCE_MAP: dict[str, dict[str, tuple[str, Literal["name", "dn"]]]] = {\n'
    )
    for owner in sorted(reference_map):
        targets2 = reference_map[owner]
        if not targets2:
            continue
        lines.append(f"    {owner!r}: {{\n")
        for target in sorted(targets2):
            rs_cls, flavor = targets2[target]
            lines.append(f"        {target!r}: ({rs_cls!r}, {flavor!r}),\n")
        lines.append("    },\n")
    lines.append("}\n\n")

    # ── TARGET_SUBCLASSES ─────────────────────────────────────────────────────
    lines.append(
        "# Abstract relation target → generated concrete subclasses.\n"
        "# Lets the resolver match a declared concrete node against a curated\n"
        "# abstract alias (domain → infraDomP → physDomP/l3extDomP/...).\n"
        "TARGET_SUBCLASSES: dict[str, list[str]] = {\n"
    )
    for abstract in sorted(target_subclasses):
        lines.append(f"    {abstract!r}: {target_subclasses[abstract]!r},\n")
    lines.append("}\n\n")

    # ── CLASS_PKG ─────────────────────────────────────────────────────────────
    lines.append(
        "# Maps full ACI class name → classPkg (e.g. 'fvBD' → 'fv').\n"
        "# Used by facade.py to locate the generated module in the right subpackage.\n"
        "CLASS_PKG: dict[str, str] = {\n"
    )
    for cls_name in sorted(class_pkg):
        lines.append(f"    {cls_name!r}: {class_pkg[cls_name]!r},\n")
    lines.append("}\n")

    return "".join(lines)


def main() -> None:
    classes = _load_schemas()
    jargon: dict[str, str] = yaml.safe_load(VOCABULARY_FILE.read_text()).get("jargon", {})
    child_map, collisions_resolved = _build_child_map(classes, jargon)
    rs_target_prop = _build_rs_target_prop(classes)
    reference_map, target_subclasses, ref_stats = _build_reference_map(classes)
    class_pkg = {cls_name: info["classPkg"] for cls_name, info in classes.items()}
    source = _render(child_map, rs_target_prop, reference_map, target_subclasses, class_pkg)
    OUT_FILE.write_text(source)
    total_entries = sum(len(v) for v in child_map.values())
    print(f"Generated {OUT_FILE}: {len(child_map)} parents, {total_entries} entries")
    print(f"  Collisions resolved: {collisions_resolved}")
    print(f"  RS_TARGET_PROP: {len(rs_target_prop)} Rs singleton classes")
    ref_pairs = sum(len(v) for v in reference_map.values())
    print(
        f"  REFERENCE_MAP: {len(reference_map)} owners, {ref_pairs} pairs "
        f"({len(target_subclasses)} abstract targets expanded; excluded: "
        f"{ref_stats['no_flavor']} without flavor, {ref_stats['no_target']} without "
        f"target, {ref_stats['ambiguous']} ambiguous after expansion)"
    )


if __name__ == "__main__":
    main()
