"""Constraint-coverage report — what the APIC actually tells us, per position.

Usage (from repo root):
    uv run python -m niwaki._codegen.report_constraints

Inputs:
    the curated vocabulary (positions) + the generated models (introspected)

Output:
    data/reports/constraint-coverage.md   (gitignored — a working document)

The question this answers: **for every curated position, what do we know
statically, and what can only be learned by probing the APIC?**

The schemas publish four constraint channels — single-field validators
(enum values, ranges, patterns), relation constraints (``relationInfo``:
cardinality / enforceable), the catalog of accepted-but-inconsistent states
(``configIssues``), and the catalog of raisable faults.  What they do *not*
publish anywhere is the **cross-field logic** (``sla_type=http`` implies
``port=80``); that logic exists only in the APIC's compiled validators, so
the only systematic way to extract it is to measure the APIC's behaviour.

This report sizes exactly that gap: per position it prints what is known
statically and how many **value pairs** would have to be probed to cover the
cross-field space (the pairwise budget of the prober).
"""

from __future__ import annotations

import math
from enum import StrEnum
from pathlib import Path
from typing import Annotated, Any, get_args, get_origin

from niwaki._codegen.generate_design import _Position, _positions
from niwaki.design._cursor import _load_class
from niwaki.models.base import ManagedObject

_REPO_ROOT = Path(__file__).parent.parent.parent.parent
OUTPUT = _REPO_ROOT / "data" / "reports" / "constraint-coverage.md"

_SKIP_FIELDS = frozenset(
    {"children", "annotation", "userdom", "description", "display_name", "owner_key", "owner_tag"}
)


def _value_space(cls: type[ManagedObject]) -> tuple[int, int, int]:
    """Return ``(enum_fields, bool_fields, int_fields)`` of a class.

    Only the fields that carry a *finite, enumerable* value space count —
    they are what a pairwise probe would vary.  Naming props and the
    housekeeping fields are excluded.
    """
    naming = set(cls._naming_props)  # pyright: ignore[reportPrivateUsage]
    enums = bools = ints = 0
    for name, info in cls.model_fields.items():
        if name in _SKIP_FIELDS or name in naming:
            continue
        ann: Any = info.annotation
        if get_origin(ann) is Annotated:
            ann = get_args(ann)[0]
        if isinstance(ann, type) and issubclass(ann, StrEnum):
            enums += 1
        elif ann is bool:
            bools += 1
        elif ann is int:
            ints += 1
    return enums, bools, ints


def _pairwise_budget(cls: type[ManagedObject]) -> int:
    """Rough size of a pairwise covering array over the class's value space.

    A pairwise (2-way) covering array needs at least ``v1 * v2`` rows, where
    ``v1``/``v2`` are the two largest value counts; in practice a greedy
    construction lands close to that bound plus a logarithmic term.  This is
    a *budget estimate* for the probing campaign, not an exact count.
    """
    naming = set(cls._naming_props)  # pyright: ignore[reportPrivateUsage]
    sizes: list[int] = []
    for name, info in cls.model_fields.items():
        if name in _SKIP_FIELDS or name in naming:
            continue
        ann: Any = info.annotation
        if get_origin(ann) is Annotated:
            ann = get_args(ann)[0]
        if isinstance(ann, type) and issubclass(ann, StrEnum):
            sizes.append(len(list(ann)))
        elif ann is bool:
            sizes.append(2)
        elif ann is int:
            sizes.append(3)  # min / default / max — three probe points
    if len(sizes) < 2:
        return sum(sizes)
    sizes.sort(reverse=True)
    return sizes[0] * sizes[1] + math.ceil(math.log2(len(sizes)))


def _row(pos: _Position) -> tuple[str, ...]:
    cls = _load_class(pos.aci_class)
    enums, bools, ints = _value_space(cls)
    issues = len(cls._config_issues)  # pyright: ignore[reportPrivateUsage]
    faults = len(cls._fault_codes)  # pyright: ignore[reportPrivateUsage]
    relation = cls._relation_info  # pyright: ignore[reportPrivateUsage]
    rel = str(relation.get("cardinality", "")) if relation else "—"
    budget = _pairwise_budget(cls)
    return (
        pos.key,
        pos.aci_class,
        f"{enums}/{bools}/{ints}",
        rel,
        str(issues or "—"),
        str(faults or "—"),
        str(budget or "—"),
    )


def render() -> str:
    """Render the whole report."""
    positions = _positions()
    rows = [_row(pos) for key, pos in sorted(positions.items()) if key]

    total_budget = sum(int(r[6]) for r in rows if r[6] != "—")
    with_issues = sum(1 for r in rows if r[4] != "—")
    with_faults = sum(1 for r in rows if r[5] != "—")

    lines = [
        "<!-- Generated by niwaki report_constraints — do not edit. -->",
        "",
        "# Constraint coverage — what the APIC tells us, per curated position",
        "",
        "**Statically known** (extracted, enforced or documented today):",
        "",
        "- single-field constraints — enum values, ranges, patterns (enforced at construction);",
        "- relation constraints — `relationInfo` cardinality / enforceable;",
        f"- accepted-but-inconsistent state catalogs — `configIssues` "
        f"({with_issues} of {len(rows)} positions);",
        f"- raisable fault catalogs — `faults` ({with_faults} of {len(rows)} positions).",
        "",
        "**Not published anywhere** — the cross-field logic (`sla_type=http` "
        "implies `port=80`, a PBR destination needs a MAC unless tracked, …). "
        "It lives only in the APIC's compiled validators, so the only "
        "systematic extractor is measurement: the probe campaign.",
        "",
        f"**Pairwise probe budget** across all curated positions: "
        f"**~{total_budget} cases** (an object push each) — the cost of "
        "covering every pair of values, class by class.",
        "",
        "| position | class | enum/bool/int | relation | configIssues | faults | pairwise |",
        "| --- | --- | --- | --- | --- | --- | --- |",
    ]
    lines.extend("| " + " | ".join(row) + " |" for row in rows)
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    """Write the constraint-coverage report."""
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT.write_text(render())
    print(f"report_constraints: wrote {OUTPUT}")


if __name__ == "__main__":
    main()
