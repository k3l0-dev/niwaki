"""Generate src/niwaki/design/_generated_cursors/ — typed design cursors.

Usage (from repo root):
    uv run python -m niwaki._codegen.generate_design

Inputs:
    src/niwaki/domain/vocabulary.yaml      (curated vocabulary)
    the generated Pydantic models          (introspected — single source of
                                            truth for field names and types)

Output:
    src/niwaki/design/_generated_cursors/  (one module per design domain)

One cursor class is emitted per curated **position** — a position is a maker
path from the ``polUni`` root (``"tenant.bd"``,
``"infra.leaf_profile.leaf_selector.node_block"``), not an ACI class: the same
class can occupy several positions (``infraNodeBlk`` under a leaf selector and
under a spine selector) and each gets its own cursor.

Scale layout (the vocabulary grows by waves):

- **Maker mixins** — each position with children gets one ``_XxxMakers``
  mixin holding its own maker methods; a cursor *inherits* the mixins of its
  whole ancestor chain (nearest first) instead of re-emitting every ancestor
  maker.  The MRO reproduces the runtime's nearest-wins resolution exactly.
- **One module per domain root** (``_tenant.py``, ``_infra.py``,
  ``_fabric.py``, ``_controller.py``; the root, its factories-eligible
  makers and the ``uni``-level domains live in ``_uni.py``), and a package
  ``__init__`` that resolves cursors **lazily** — importing the design and
  building a tenant never parses the infra/fabric modules.
- ``CURSOR_FOR`` stays the runtime contract (:mod:`niwaki.design._cursor`),
  now a lazy mapping backed by a tiny position → (module, class) index.

Each cursor carries fully-typed signatures — makers, a typed ``set()``,
typed ``bind()`` aliases, contract verbs — while every body delegates to the
dynamic runtime in :mod:`niwaki.design._cursor`.  The generated package adds
*types only*.

Design note: field names and annotations are **introspected from the
generated models** rather than re-derived from ``sdk_subset.json`` — the
models already encode the D2 naming and enum mapping, so introspection
cannot drift from them.
"""

from __future__ import annotations

import sys
from enum import StrEnum
from pathlib import Path
from typing import Annotated, Literal, NamedTuple, get_args, get_origin

from niwaki.design._cursor import _load_class, _tables
