_REPO_ROOT = Path(__file__).parent.parent.parent.parent
OUTPUT_DIR = _REPO_ROOT / "src" / "niwaki" / "design" / "_generated_cursors"
_LEGACY_FILE = OUTPUT_DIR.with_suffix(".py")

