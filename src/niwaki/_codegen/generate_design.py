class _Position(NamedTuple):
    """One curated position in the maker tree rooted at ``polUni``.

    Attributes:
        key: Dotted maker path from the root (``""`` for the root itself).
        label: Maker name that creates this position (``"uni"`` for the root).
        aci_class: ACI class occupying the position.
        parent_key: Key of the parent position (``None`` for the root).
    """

    key: str
    label: str
    aci_class: str
    parent_key: str | None


# Base-cursor methods the generated classes narrow ON PURPOSE (typed
# signatures delegating to the runtime) — everything else is off-limits.
_TYPED_NARROWINGS = frozenset({"set", "bind", "bind_dn", "provide", "consume"})


def _reserved_cursor_api() -> frozenset[str]:
    """Names the base :class:`Cursor` owns — maker/verb labels must not shadow them.

    A curated label equal to ``design_node``, ``dn``, ``mo``, ``push`` or
    ``to_payload`` would silently replace that API on the affected cursors
    (the way a ``node`` maker would have shadowed the old ``node`` property).
    """
    from niwaki.design._cursor import Cursor

    return frozenset(name for name in dir(Cursor) if not name.startswith("_")) - _TYPED_NARROWINGS


def _positions() -> dict[str, _Position]:
    """Derive every curated position by walking the makers tree from polUni.

    Raises:
        SystemExit: The makers table contains a class cycle (a class reachable
            from itself), or a maker/verb label shadows the base cursor API
            (``set``, ``bind``, ``push``, ``design_node``, …) — generated
            methods would silently break those calls on the affected cursors.
    """
    tables = _tables()
    reserved = _reserved_cursor_api()
    labels = {label for table in tables.makers.values() for label in table} | {
        verb for table in tables.verbs.values() for verb in table
    }
    if shadowing := sorted(labels & reserved):
        sys.exit(f"generate_design: labels shadow the base Cursor API: {shadowing}")

    positions: dict[str, _Position] = {"": _Position("", "uni", "polUni", None)}

    def _walk(parent_key: str, parent_class: str, lineage: tuple[str, ...]) -> None:
        for label, child in tables.makers.get(parent_class, {}).items():
            if child in lineage:
                sys.exit(f"generate_design: class cycle through {child} at {parent_key!r}")
            key = f"{parent_key}.{label}" if parent_key else label
            positions[key] = _Position(key, label, child, parent_key)
            _walk(key, child, (*lineage, child))

    _walk("", "polUni", ("polUni",))
    return positions


def _camel(label: str) -> str:
    return "".join(part.capitalize() for part in label.split("_"))


def _cursor_names(positions: dict[str, _Position]) -> dict[str, str]:
    """Map position key → generated cursor class name.

    Same-label collisions (two ``node_block`` positions) are disambiguated by
    prefixing the parent label (``LeafSelectorNodeBlockCursor``).

    Raises:
        SystemExit: Two positions still collide after parent-label prefixing.
    """
    by_label: dict[str, list[_Position]] = {}
    for pos in positions.values():
        if pos.key:
            by_label.setdefault(pos.label, []).append(pos)

    names: dict[str, str] = {"": "UniCursor"}
    for label, group in by_label.items():
        for pos in group:
            if len(group) == 1:
                names[pos.key] = f"{_camel(label)}Cursor"
            else:
                parent = positions[pos.parent_key or ""]
                names[pos.key] = f"{_camel(parent.label)}{_camel(label)}Cursor"
    if len(set(names.values())) != len(names):
        sys.exit("generate_design: cursor name collision after parent-label prefixing")
    return names


def _module_of(key: str) -> str:
    """Module basename hosting the position *key* (domain split)."""
    first = key.split(".", 1)[0]
    return _DOMAIN_MODULES.get(first, "_uni")


def _mixin_name(cursor_name: str) -> str:
    """Maker-mixin class name paired with *cursor_name*."""
    return f"_{cursor_name.removesuffix('Cursor')}Makers"


def _chain(pos: _Position, positions: dict[str, _Position]) -> list[_Position]:
    """Return ``[pos, parent, …, root]``."""
    levels = [pos]
    while levels[-1].parent_key is not None:
        levels.append(positions[levels[-1].parent_key])
    return levels


def _field_params(cls: type[ManagedObject]) -> tuple[list[tuple[str, str]], set[tuple[str, str]]]:
    """Introspect typed keyword parameters for *cls* (non-naming fields).

    Returns:
        ``(params, enum_imports)`` where *params* is ``[(py_name, type_str)]``
        in model declaration order and *enum_imports* is a set of
        ``(module, class_name)`` pairs needed under ``TYPE_CHECKING``.
    """
    naming = set(cls._naming_props)  # pyright: ignore[reportPrivateUsage]
    params: list[tuple[str, str]] = []
    enum_imports: set[tuple[str, str]] = set()
    for name, info in cls.model_fields.items():
        if name == "children" or name in naming:
            continue
        ann = info.annotation
        if get_origin(ann) is Annotated:
            ann = get_args(ann)[0]
        if ann is bool:
            type_str = "bool"
        elif isinstance(ann, type) and issubclass(ann, StrEnum):
            type_str = f"{ann.__name__} | str"
            enum_imports.add((ann.__module__, ann.__name__))
        elif ann is int:
            type_str = "int"
        elif get_origin(ann) is Literal:
            type_str = "str"
        else:
            type_str = "str"
        params.append((name, f"{type_str} | None"))
    return params, enum_imports


def _sugar_params(aci_class: str) -> list[tuple[str, str]]:
    """Curated sugar parameters of *aci_class* (from ``vocabulary.yaml``)."""
    return list(_tables().sugar.get(aci_class, {}).items())


def _alias_targets_by_dn(owner_aci_class: str, target_aci_class: str) -> bool:
    """True when the (owner → target) relation is direct with flavor ``dn``.

    Those aliases (and only those) qualify for the typed ``bind_dn()``
    surface — mirrors the runtime check in ``Cursor.bind_dn``.
    """
    from niwaki.domain._child_map import REFERENCE_MAP, TARGET_SUBCLASSES

    direct = REFERENCE_MAP.get(owner_aci_class, {})
    entries = {
        entry
        for candidate in (target_aci_class, *TARGET_SUBCLASSES.get(target_aci_class, ()))
        if (entry := direct.get(candidate)) is not None
    }
    return len(entries) == 1 and next(iter(entries))[1] == "dn"


def _naming_params(cls: type[ManagedObject]) -> list[str]:
    """Naming props of *cls* as required positional parameter names."""
    return list(cls._naming_props)  # pyright: ignore[reportPrivateUsage]


def _render_signature(
    method: str,
    naming: list[str],
    keyword_params: list[tuple[str, str]],
    return_type: str,
) -> list[str]:
    """Render a def line with one parameter per line (ruff-format friendly)."""
    lines = [f"    def {method}(", "        self,"]
    lines.extend(f"        {p}: str," for p in naming)
    if keyword_params:
        lines.append("        *,")
        lines.extend(f"        {name}: {type_str} = None," for name, type_str in keyword_params)
    lines.append(f"    ) -> {return_type}:")
    return lines


def _maker_body(label: str, naming: list[str], return_type: str) -> list[str]:
    exclude = ", ".join(repr(n) for n in ("self", *naming))
    args = ", ".join(naming)
    args_tuple = f"({args},)" if naming else "()"
    return [
        f"        params = {{k: v for k, v in locals().items() if k not in ({exclude},)}}",
        "        return cast(",
        f'            "{return_type}",',
        f'            self._invoke_maker("{label}", {args_tuple}, _prune(params)),',
        "        )",
    ]


def _render_maker(
    label: str,
    child_aci: str,
    return_type: str,
    enum_imports: set[tuple[str, str]],
    owner_label: str,
) -> list[str]:
    child_cls = _load_class(child_aci)
    naming = _naming_params(child_cls)
    params, imports = _field_params(child_cls)
    enum_imports |= imports
    keyword_params = _sugar_params(child_aci) + params
    lines = _render_signature(label, naming, keyword_params, return_type)
    lines.append(f'        """Declare a ``{child_aci}`` child under the {owner_label} level."""')
    lines.extend(_maker_body(label, naming, return_type))
    lines.append("")
    return lines


def _render_mixin(
    pos: _Position,
    positions: dict[str, _Position],
    names: dict[str, str],
    enum_imports: set[tuple[str, str]],
) -> list[str]:
    """Render the maker mixin of *pos* — its OWN makers only.

    Descendant cursors inherit this mixin instead of re-emitting the makers;
    the inheritance order (nearest ancestor first) reproduces the runtime's
    nearest-wins label resolution.
    """
    tables = _tables()
    mixin = _mixin_name(names[pos.key])
    lines = [
        f"class {mixin}(Cursor):",
        f'    """Makers declared at the {pos.label} level (``{pos.aci_class}``)."""',
        "",
        "    __slots__ = ()",
        "",
    ]
    for maker_label, child_aci in tables.makers.get(pos.aci_class, {}).items():
        child_key = f"{pos.key}.{maker_label}" if pos.key else maker_label
        lines.extend(
            _render_maker(maker_label, child_aci, names[child_key], enum_imports, pos.label)
        )
    return lines


def _render_cursor(
    pos: _Position,
    positions: dict[str, _Position],
    names: dict[str, str],
    enum_imports: set[tuple[str, str]],
) -> list[str]:
    tables = _tables()
    cls = _load_class(pos.aci_class)
    cursor_name = names[pos.key]
    chain = _chain(pos, positions)

    bases = [
        _mixin_name(names[level.key])
        for level in chain
        if tables.makers.get(level.aci_class)  # only levels that declare makers
    ] or ["Cursor"]

    lines = [
        f"class {cursor_name}({', '.join(bases)}):",
        f'    """Typed cursor for ``{pos.aci_class}`` ({pos.label} level).',
        "",
        f"    Position: ``uni{'.' + pos.key if pos.key else ''}``",
        "",
        "    Ancestor makers (implicit pop) come from the inherited mixins,",
        "    nearest level first — the MRO mirrors the runtime resolution.",
        '    """',
        "",
        "    __slots__ = ()",
        "",
    ]

    # set() — typed scalar attributes of this class.
    params, imports = _field_params(cls)
    enum_imports |= imports
    keyword_params = _sugar_params(pos.aci_class) + params
    lines.extend(_render_signature("set", [], keyword_params, cursor_name))
    lines.append(f'        """Set ``{pos.aci_class}`` attributes (merged; validated eagerly)."""')
    lines.append('        params = {k: v for k, v in locals().items() if k != "self"}')
    lines.append("        Cursor.set(self, **_prune(params))")
    lines.append("        return self")
    lines.append("")

    # bind() — aliases available on this level and every ancestor level.
    aliases: list[str] = []
    dn_aliases: list[str] = []
    for level in chain:
        for alias, target in tables.binds.get(level.aci_class, {}).items():
            if alias in aliases:
                continue
            aliases.append(alias)
            if _alias_targets_by_dn(level.aci_class, target):
                dn_aliases.append(alias)
    if aliases:
        alias_params = [(a, "str | None") for a in aliases]
        lines.extend(_render_signature("bind", [], alias_params, cursor_name))
        lines.append('        """Declare lazy Rs references (resolved at push time)."""')
        lines.append('        params = {k: v for k, v in locals().items() if k != "self"}')
        lines.append("        Cursor.bind(self, **_prune(params))")
        lines.append("        return self")
        lines.append("")
    if dn_aliases:
        alias_params = [(a, "str | None") for a in dn_aliases]
        lines.extend(_render_signature("bind_dn", [], alias_params, cursor_name))
        lines.append('        """Reference objects outside the design by raw DN."""')
        lines.append('        params = {k: v for k, v in locals().items() if k != "self"}')
        lines.append("        Cursor.bind_dn(self, **_prune(params))")
        lines.append("        return self")
        lines.append("")

    # Contract verbs (typed narrowing of the runtime methods).
    if tables.verbs.get(pos.aci_class):
        for verb in tables.verbs[pos.aci_class]:
            lines.append(f"    def {verb}(self, contract: str) -> {cursor_name}:")
            lines.append(f'        """Declare this EPG {verb}s *contract* (lazy)."""')
            lines.append(f"        Cursor.{verb}(self, contract)")
            lines.append("        return self")
            lines.append("")

    return lines


def _render_domain_module(
    module: str,
    keys: list[str],
    positions: dict[str, _Position],
    names: dict[str, str],
) -> str:
    """Render one domain module: mixins + cursors for its positions."""
    tables = _tables()
    enum_imports: set[tuple[str, str]] = set()
    bodies: list[str] = []

    for key in sorted(keys, key=lambda k: (k.count("."), k)):
        pos = positions[key]
        if tables.makers.get(pos.aci_class):
            bodies.extend(_render_mixin(pos, positions, names, enum_imports))
            bodies.append("")
        bodies.extend(_render_cursor(pos, positions, names, enum_imports))
        bodies.append("")

    # Runtime imports: ancestor mixins living in _uni (the root's mixin).
    runtime_imports = ""
    if module != "_uni":
        runtime_imports = "from niwaki.design._generated_cursors._uni import _UniMakers\n"

    # TYPE_CHECKING imports: enums + (for _uni) the cross-module cursor names
    # returned by the root makers.
    type_lines = [f"    from {mod} import {name}" for mod, name in sorted(enum_imports)]
    if module == "_uni":
        cross: dict[str, list[str]] = {}
        for key in positions:
            if key and "." not in key and _module_of(key) != "_uni":
                cross.setdefault(_module_of(key), []).append(names[key])
        for mod, cursor_names in sorted(cross.items()):
            joined = ", ".join(sorted(cursor_names))
            type_lines.append(f"    from niwaki.design._generated_cursors.{mod} import {joined}")
    header = _MODULE_HEADER.format(
        domain=module.lstrip("_"),
        runtime_imports=runtime_imports,
        type_imports="\n".join(type_lines) or "    pass",
    )
    return "\n".join([header, "", *bodies, ""])


def _render_factory(
    label: str,
    pos: _Position,
    names: dict[str, str],
    enum_imports: set[tuple[str, str]],
) -> list[str]:
    """Render a module-level root factory (``tenant()``, ``infra()``, …)."""
    cls = _load_class(pos.aci_class)
    naming = _naming_params(cls)
    params, imports = _field_params(cls)
    enum_imports |= imports
    return_type = names[pos.key]

    lines = [f"def {label}("]
    lines.extend(f"    {p}: str," for p in naming)
    if params:
        lines.append("    *,")
        lines.extend(f"    {n}: {t} = None," for n, t in params)
    lines.append(f") -> {return_type}:")
    lines.extend(
        [
            f'    """Create a detached design and declare its ``{pos.aci_class}`` root.',
            "",
            f"    Shorthand for ``design().{label}(...)`` — the returned cursor",
            "    sits on the declared object; the design root is the enclosing",
            "    ``polUni`` node, so sibling domains stay reachable through the",
            "    implicit-pop makers.",
            "",
            "    Returns:",
            f"        :class:`{return_type}` on the new ``{pos.aci_class}`` node.",
            '    """',
        ]
    )
    exclude = ", ".join(repr(n) for n in naming) + ("," if naming else "")
    args = ", ".join(naming)
    lines.append(f"    params = {{k: v for k, v in locals().items() if k not in ({exclude})}}")
    call_args = f"{args}, **_prune(params)" if args else "**_prune(params)"
    lines.append(f"    return design().{label}({call_args})")
    lines.append("")
    return lines


