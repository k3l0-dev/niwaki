def render_all() -> dict[str, str]:
    """Render every package file as ``{filename: content}``."""
    positions = _positions()
    names = _cursor_names(positions)

    by_module: dict[str, list[str]] = {}
    for key in positions:
        by_module.setdefault(_module_of(key), []).append(key)

    files = {
        f"{module}.py": _render_domain_module(module, keys, positions, names)
        for module, keys in sorted(by_module.items())
    }
    files["__init__.py"] = _render_init(positions, names)
    return files


def main() -> None:
    """Generate and write the typed cursors package."""
    if _LEGACY_FILE.exists():
        _LEGACY_FILE.unlink()  # the pre-package single-file layout
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    files = render_all()
    for stale in OUTPUT_DIR.glob("*.py"):
        if stale.name not in files:
            stale.unlink()
    for filename, content in files.items():
        (OUTPUT_DIR / filename).write_text(content, encoding="utf-8")
    print(f"generate_design: wrote {len(files)} modules to {OUTPUT_DIR}")


if __name__ == "__main__":
    main()
