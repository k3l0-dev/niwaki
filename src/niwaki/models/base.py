"""ManagedObject — base class for all generated ACI primitives.

Every ACI class generated by the codegen inherits from ManagedObject.
The class itself is never instantiated directly.

Design notes:
- __init_subclass__ auto-registers each subclass in REGISTRY (no metaclass needed,
  avoids conflict with Pydantic's ModelMetaclass).
- model_fields_set is used by to_apic() to send only explicitly set fields, plus
  naming props — producing surgical POSTs instead of full object serialisation.
- extra="allow" absorbs APIC read-only props (modTs, uid, lcOwn …) from GET
  responses without raising validation errors.
- model_rebuild() is called at module level to resolve the self-referential
  children: list[ManagedObject] annotation.

Codegen contract — the ClassVars every generated subclass declares (they are
private: schema metadata for the SDK's own machinery, not a public API, and
deliberately kept out of the rendered class documentation):

- ``_aci_class``       ACI class name (``"fvBD"``).
- ``_rn_format``       RN format string (``"BD-{name}"``).
- ``_naming_props``    Property names forming the RN (``["name"]``).
- ``_secure_props``    Write-only props (passwords, pre-shared keys) — the APIC
                       never echoes them, so diff and plan must skip them.
- ``_config_issues``   The APIC's declared catalog of accepted-but-inconsistent
                       states (``{code: description}``, from ``configIssues``).
- ``_fault_codes``     Faults the APIC can raise on the class
                       (``{"F2305": "fltFvBDMulticastEnabledOnL2BD"}``).
- ``_relation_info``   Rs classes only: ``cardinality`` / ``enforceable`` /
                       ``resolvable``, from the schema's ``relationInfo``.
- ``_contains``        ACI class names valid as direct children.
- ``_mo_category``     APIC object category (``"Regular"``, ``"RelationshipToLocal"``).
- ``_write_access``    APIC privilege roles allowed to write the class.
- ``_is_creatable``    The APIC accepts a POST that creates the class (``always``);
  ``never`` (default singletons, non-creatable carriers) and ``derived`` are False.
- ``_is_observable``   APIC schema metadata, informational only — empirically
  found *not* to reliably gate WebSocket subscription eligibility (a class
  with ``_is_observable=False`` was live-confirmed to accept a subscription
  and deliver real pushes). Never used as a subscribe-time gate; see
  :class:`~niwaki.exceptions.StatsClassNotSubscribableError` for the flag
  that actually governs it (``isStat``).
- ``_is_faultable``    The APIC can raise faults on the class.
- ``_is_health_scorable`` The APIC tracks a health score for instances.
- ``_has_stats``       Statistics counters exist as children.
"""

from __future__ import annotations

from typing import Any, ClassVar, Self

from pydantic import BaseModel, ConfigDict, PrivateAttr

from niwaki.models._wire import from_wire, parse_flags, to_wire

REGISTRY: dict[str, type[ManagedObject]] = {}
"""ACI class name → generated Python class, for every imported model.

Populated by ``__init_subclass__`` as model modules are imported (lazily, on
first use), and used by ``from_apic`` to deserialise an APIC answer into the
right typed class.
"""

# Per-class alias map cache: avoids recomputing {aci_alias → python_field} on every from_apic().
_ALIAS_MAP_CACHE: dict[type[ManagedObject], dict[str, str]] = {}


def _coerce_apic_value(annotation: Any, value: Any) -> Any:
    """Coerce a raw APIC attribute onto the field's declared Python type.

    Thin seam over :func:`niwaki.models._wire.from_wire`: ``from_apic`` builds
    instances with ``model_construct``, so no validator ever runs and this is
    the only thing that gives a read-back field its declared type.

    Args:
        annotation: The Pydantic field annotation (possibly ``Annotated``).
        value: Raw value from the APIC attributes dict.

    Returns:
        The coerced value, or the original when the type keeps the wire form.
    """
    return from_wire(annotation, value)


# A catalogue FieldKind ("bool"/"int"/"float") maps to the Python type whose wire
# coercer (via ``from_wire``) is the SINGLE source of truth for reading it back.
_KIND_TO_TYPE: dict[str, type] = {"bool": bool, "int": int, "float": float}


def _coerce_read(value: object, kind: str | None) -> object:
    """Coerce a raw wire string by its catalogue *kind* (not a Python annotation).

    The wire boundary (:func:`niwaki.models._wire.from_wire`) coerces by a field's
    annotation; a non-generated class has no annotation, only the catalogue's
    :class:`~niwaki._codegen.basetypes.FieldKind`.  This is the read-side twin, and
    it reuses the very same coercers so a value reads **identically** whether its
    class is generated or not: ``"yes"``/``"1"``/``"on"`` → ``True``, ``"0xff"`` →
    ``255``, ``"public,shared"`` → a ``frozenset`` of member names; and — like the
    wire boundary — anything the type cannot hold (a sentinel like
    ``"not-applicable"``) is returned **untouched**.  Enums, addresses and free
    strings keep the wire string (a non-generated class has no ``StrEnum`` to
    instantiate; a ``StrEnum`` compares equal to its value, so equality holds).
    """
    if kind is None or not isinstance(value, str):
        return value
    if kind == "flags":
        return frozenset(parse_flags(value))
    py_type = _KIND_TO_TYPE.get(kind)
    if py_type is None:
        return value  # enum / ip / mac / named_number / str keep the wire string
    return from_wire(py_type, value)


_MISS: object = object()
"""Sentinel: a name the catalogue does not resolve (fall back to Pydantic)."""


class ManagedObject(BaseModel):
    """Base class for every ACI Managed Object.

    The 2,222 ACI classes are generated subclasses; this base provides the
    runtime behaviour they all share — RN computation, child containment
    validation, APIC serialisation (surgical: only the fields you set), and
    deserialisation back into the right typed class.

    Instances validate at construction: a wrong enum value, an out-of-range
    integer or a malformed IP never reaches the wire.

    Example::

        from niwaki.models.fv.fvBD import fvBD

        bd = fvBD(name="prod-bd", unicast_routing=True)
        bd.rn                 # → "BD-prod-bd"
        bd.to_apic()
        # → {"fvBD": {"attributes": {"name": "prod-bd", "unicastRoute": "true"}}}

    Every generated subclass also carries private ClassVars holding the APIC
    schema metadata (naming props, containment, fault and config-issue
    catalogs…) — they are the codegen contract, not a public API; see the
    module docstring for the full list.
    """

    model_config = ConfigDict(extra="allow", populate_by_name=True)

    # The wire class name (e.g. "fvCEp") of an object read from the APIC.  A
    # generated subclass knows its class from ``_aci_class``; this is what lets a
    # *non-generated* object look itself up in the read catalogue for readable
    # field access.  Empty on objects built locally rather than read back.
    _wire_class: str = PrivateAttr(default="")

    _aci_class: ClassVar[str] = ""
    _rn_format: ClassVar[str] = ""
    _naming_props: ClassVar[list[str]] = []
    _secure_props: ClassVar[frozenset[str]] = frozenset()
    _config_issues: ClassVar[dict[str, str]] = {}
    _fault_codes: ClassVar[dict[str, str]] = {}
    _relation_info: ClassVar[dict[str, str | bool]] = {}
    _contains: ClassVar[frozenset[str]] = frozenset()
    _mo_category: ClassVar[str] = "Regular"
    _write_access: ClassVar[frozenset[str]] = frozenset()
    _is_creatable: ClassVar[bool] = True
    _is_observable: ClassVar[bool] = False
    _is_faultable: ClassVar[bool] = False
    _is_health_scorable: ClassVar[bool] = False
    _has_stats: ClassVar[bool] = False

    children: list[ManagedObject] = []

    @classmethod
    def _get_alias_map(cls) -> dict[str, str]:
        """Return a cached ``{aci_alias: python_field_name}`` mapping for this class.

        Built once per concrete class and stored in the module-level
        ``_ALIAS_MAP_CACHE`` so that repeated calls (e.g. bulk ``from_apic()``
        deserialisation) pay only a single dict lookup rather than a full
        comprehension over ``model_fields``.

        The wire name lives in ``validation_alias``/``serialization_alias``, not
        in ``alias``: the plain ``alias=`` is the one PEP 681 reads, and a type
        checker that reads it synthesises an ``__init__`` taking ``arpFlood`` and
        rejecting ``arp_flooding`` — the readable name would be an error in the
        user's editor while working at runtime.  The two-sided spelling keeps the
        wire name for pydantic and the Python name for the type checker.

        Returns:
            Dict mapping ACI camelCase attribute names to snake_case Python field
            names, for every field whose wire name differs from its Python name.
        """
        if cls not in _ALIAS_MAP_CACHE:
            _ALIAS_MAP_CACHE[cls] = {
                info.validation_alias: name
                for name, info in cls.model_fields.items()
                if isinstance(info.validation_alias, str)
            }
        return _ALIAS_MAP_CACHE[cls]

    def __init_subclass__(cls, **kwargs: Any) -> None:
        """Auto-register every concrete subclass in REGISTRY.

        Called automatically by Python when a class inherits from ManagedObject.
        Only registers when _aci_class is non-empty (concrete classes).

        Args:
            **kwargs: Forwarded to super().__init_subclass__.
        """
        super().__init_subclass__(**kwargs)
        if aci_class := getattr(cls, "_aci_class", None):
            REGISTRY[aci_class] = cls

    # ── DN / RN ───────────────────────────────────────────────────────────────

    @property
    def rn(self) -> str:
        """Compute the Relative Name from _rn_format and current naming prop values.

        Returns:
            RN string with all ``{prop}`` placeholders replaced.

        Example::

            bd = fvBD(name="web")
            bd.rn  # → "BD-web"
        """
        rn = self._rn_format
        for prop in self._naming_props:
            value = getattr(self, prop, "")
            wire = "" if value is None else to_wire(value)
            rn = rn.replace(f"{{{prop}}}", wire)
        return rn

    @property
    def dn(self) -> str:
        """The APIC Distinguished Name, as returned on a read.

        A read result always carries ``dn`` — it is structural, present even
        under :meth:`~niwaki.query.Query.naming_only` and
        :meth:`~niwaki.query.Query.config_only` — and it lands in ``model_extra``
        because it is not a configurable field.  A locally-constructed object has
        no DN yet, so accessing ``.dn`` on one raises :class:`AttributeError`.

        This reads the same whether the class is generated or not — one uniform
        accessor over the whole readable object space.

        Returns:
            The Distinguished Name string.

        Raises:
            AttributeError: The object was built locally, not read from the
                APIC, so no ``dn`` is present.
        """
        extra = self.model_extra
        if extra is not None and "dn" in extra:
            return str(extra["dn"])
        raise AttributeError(
            f"{type(self).__name__!r} object has no 'dn': it was constructed "
            "locally, not read from the APIC"
        )

    def __getitem__(self, key: str) -> object:
        """Read any attribute by its APIC (wire) name — uniform across all classes.

        The escape hatch for reading an object the way the APIC names it, without
        caring whether the class is one of the generated models (typed fields) or
        an operational class absorbed into ``model_extra``.  Resolution order:
        the read-only APIC attributes (``model_extra``), then a renamed config
        field (by its wire alias), then a config field whose wire name equals its
        Python name.

        Args:
            key: An APIC wire name (``"arpFlood"``) or the Python field name
                (``"arp_flooding"``); read-only APIC attributes such as ``"dn"``
                or ``"role"`` are addressed by their wire name.

        Returns:
            The attribute value — a typed value for a config field, the raw wire
            value for an APIC-originated attribute.

        Raises:
            TypeError: *key* is not a string.
            KeyError: No attribute by that wire name exists on this object.
        """
        # Runtime guard for dynamically-typed callers: a non-str key (e.g. an
        # int index or a slice) must fail loudly as a TypeError, not silently as
        # a KeyError.  Pyright sees ``key: str`` and flags this as unnecessary.
        if not isinstance(key, str):  # pyright: ignore[reportUnnecessaryIsInstance]
            raise TypeError(
                "managed-object subscription takes a wire attribute name (str), "
                f"not {type(key).__name__}"
            )
        extra = self.model_extra
        if extra is not None and key in extra:
            return extra[key]
        alias_map = self._get_alias_map()  # {wire_alias: python_field}
        if key in alias_map:
            return getattr(self, alias_map[key])
        if key in type(self).model_fields:
            return getattr(self, key)
        raise KeyError(key)

    @property
    def attrs(self) -> dict[str, str]:
        """The object's attributes as APIC wire strings — one flat view for any class.

        Every config field, at its current value, is rendered back to wire form
        (``True`` → ``"true"``, a flags set → ``"a,b"``) under its wire name, then
        merged with the read-only attributes the APIC returned (``model_extra``,
        already wire strings); ``children`` is excluded.  A generated object and
        an operational one read identically through this one call — it never
        exposes the typed/dynamic split.

        This is the object's **current state** re-rendered to wire form, not a
        verbatim copy of the APIC response: a generated object read from the APIC
        carries every field (:meth:`from_apic` builds a complete instance), so
        ``attrs`` includes defaults the APIC may not have sent, and a value is
        re-rendered (a bool becomes ``"true"``/``"false"``) and so can differ
        from the exact string the APIC returned.

        Returns:
            ``{wire_attribute_name: wire_string_value}``.
        """
        wire_of = {python: wire for wire, python in self._get_alias_map().items()}
        out: dict[str, str] = {}
        for name in type(self).model_fields:
            if name == "children":
                continue
            value = getattr(self, name)
            if value is None:
                continue
            out[wire_of.get(name, name)] = to_wire(value)
        extra = self.model_extra
        if extra:
            for key, value in extra.items():
                out[key] = value if isinstance(value, str) else to_wire(value)
        return out

    def __getattr__(self, name: str) -> Any:
        """Model fields and extras resolve normally; readable names via the catalogue.

        A generated object answers ``bd.arp_flooding`` from its typed field.  A
        **non-generated** object (a learned endpoint, a stat), or a **read-only**
        property a model omits, has no such field — so the readable name is
        recovered from the read catalogue: readable → wire → the value in
        ``model_extra``, coerced by the property's kind (``"yes"`` → ``True``,
        ``"public,shared"`` → a ``frozenset``).  **Every** catalogue property
        coerces this way, whether its readable name was renamed (``arpFlood`` →
        ``arp_flooding``) or spelled like its wire name (``scope``); the attribute
        is the typed view, ``mo["wireName"]`` stays the raw wire view.

        Raises ``AttributeError`` — like any missing attribute — when the object
        was built locally (no wire class), the catalogue is absent, or the name is
        neither a field, an extra, nor a readable property of the class.
        """
        if not name.startswith("_"):
            # A property/method on the class that raises AttributeError (``.dn`` on
            # a locally-built object) re-enters here; re-run it so its own message
            # stands rather than being masked by catalogue resolution.
            if hasattr(type(self), name):
                return object.__getattribute__(self, name)
            # The catalogue coerces every known property — consulted *before* the
            # raw extras so a homonym (``scope`` → ``scope``) is typed, not a string.
            resolved = self._catalogue_read(name)
            if resolved is not _MISS:
                return resolved
        # Pydantic resolves genuine extras (dn, modTs, uid…), private attrs, and the
        # final "no such attribute" (including the ``_``-prefixed names skipped here).
        return super().__getattr__(name)  # type: ignore[misc]  # BaseModel defines it

    def _catalogue_read(self, name: str) -> Any:
        """Coerced value of a readable property via the catalogue, or :data:`_MISS`.

        Returns :data:`_MISS` — so the caller falls back to Pydantic — when the
        object has no wire class, the catalogue is unavailable, or *name* is not
        one of the class's readable properties (or its value was not returned).
        """
        private = object.__getattribute__(self, "__pydantic_private__") or {}
        wire_class = private.get("_wire_class", "")
        if not wire_class:
            return _MISS
        # Function-local import: the catalogue subsystem stays off the import path
        # of ``niwaki`` until the first readable access on a non-generated object.
        from niwaki.query._catalog import catalog

        try:
            meta = catalog().class_meta(wire_class)
        except (KeyError, FileNotFoundError):
            return _MISS
        wire = meta.readable_to_wire.get(name)
        if wire is None:
            return _MISS
        extra = object.__getattribute__(self, "__pydantic_extra__") or {}
        if wire not in extra:
            return _MISS  # known property, but the APIC did not return its value
        return _coerce_read(extra[wire], meta.wire_to_kind.get(wire))

    # ── Serialisation ─────────────────────────────────────────────────────────

    def to_apic(self) -> dict[str, Any]:
        """Serialise this object to the APIC JSON envelope format.

        Only fields that were explicitly set at construction time (tracked via
        ``model_fields_set``) are included in attributes, plus any naming props.
        This produces surgical POSTs — the APIC applies its own defaults for
        unset fields.

        Extra fields absorbed from APIC GET responses (``model_extra``) are never
        included in the serialised output.

        Returns:
            Dict in APIC envelope format::

                {
                    "fvBD": {
                        "attributes": {"name": "web", "unicastRoute": "true"},
                        "children": [...]   # only when children exist
                    }
                }

        Example::

            bd = fvBD(name="web", unicast_routing=True)
            bd.to_apic()
            # → {"fvBD": {"attributes": {"name": "web", "unicastRoute": "true"}}}
        """
        # Only send defined model fields (not APIC-originated extra fields)
        model_field_names = set(self.__class__.model_fields.keys()) - {"children"}
        fields_to_send = set(self._naming_props) | (self.model_fields_set & model_field_names)

        naming_set = set(self._naming_props)
        attrs: dict[str, Any] = {}
        for field_name in fields_to_send:
            val = getattr(self, field_name, None)
            if val is None:
                continue
            # Skip empty strings for optional fields — sending "" would silently
            # overwrite existing APIC values with an empty string. Naming props
            # are always included even when empty (the APIC requires them).
            if val == "" and field_name not in naming_set:
                continue
            # A renamed field carries the ACI attribute name in its
            # serialization_alias (arp_flooding → arpFlood, from_ → from).
            field_info = self.__class__.model_fields.get(field_name)
            aci_key = (
                field_info.serialization_alias
                if field_info and field_info.serialization_alias
                else field_name
            )
            attrs[aci_key] = to_wire(val)

        envelope: dict[str, Any] = {self._aci_class: {"attributes": attrs}}
        if self.children:
            envelope[self._aci_class]["children"] = [child.to_apic() for child in self.children]
        return envelope

    # ── Surgical construction ─────────────────────────────────────────────────

    @classmethod
    def surgical(cls, naming: dict[str, Any], **changes: Any) -> Self:
        """Construct a payload-only instance for surgical APIC writes.

        Builds an instance via ``model_construct`` with ``model_fields_set``
        limited to the naming props plus the explicitly provided *changes*.
        Calling ``to_apic()`` on the result sends only those fields — the APIC
        leaves all other attributes untouched (APIC upsert semantics).

        This is the canonical alternative to calling ``model_construct`` with
        ``_fields_set`` directly: it centralises the logic and prevents the
        common mistake of forgetting to include naming props in ``_fields_set``.

        Args:
            naming: Dict of naming prop values (e.g. ``{"name": "web"}``).
            **changes: Attribute values to include in the payload.

        Returns:
            A partially-populated instance ready for :meth:`to_apic`.

        Example::

            # Surgical update: only name + arp_flooding in the POST payload
            delta = fvBD.surgical({"name": "web"}, arp_flooding=True)
            delta.to_apic()
            # → {"fvBD": {"attributes": {"name": "web", "arpFlood": "true"}}}
        """
        return cls.model_construct(
            _fields_set=set(cls._naming_props) | set(changes.keys()),
            **{**naming, **changes},
        )

    # ── Deserialisation ───────────────────────────────────────────────────────

    @classmethod
    def _dispatch_and_coerce(
        cls, data: dict[str, Any]
    ) -> tuple[type[ManagedObject], dict[str, Any], dict[str, Any], str]:
        """Shared first half of :meth:`from_apic`/:meth:`from_event`: dispatch, alias, coerce.

        Args:
            data: One envelope, e.g. ``{"fvBD": {"attributes": {...}}}``.

        Returns:
            ``(target_cls, coerced, inner, aci_class)`` — the REGISTRY-resolved
            subclass, the alias-remapped and type-coerced attributes, the raw
            inner envelope (attributes + children, for the caller to recurse
            on), and the wire class name.
        """
        aci_class = next(iter(data))
        target_cls: type[ManagedObject] = REGISTRY.get(aci_class, cls)
        inner = data[aci_class]

        # Use model_construct to bypass Pydantic validation:
        # - APIC returns sentinel strings like "not-applicable" for unset fields
        #   that don't match strict pattern validators (e.g. MAC address patterns).
        # - We trust the APIC to return well-formed data.
        # - Extra APIC fields (dn, modTs, uid, …) land in __pydantic_extra__
        #   because extra="allow" is set on the model config.
        # - _coerce_apic_value converts "yes"/"no" strings to Python bools (and
        #   digit-strings to int) so that mo_diff comparisons are type-correct.
        raw_attrs: dict[str, Any] = inner.get("attributes", {})

        # Remap ACI wire names → Python field names via Field aliases.
        # After D2 codegen, model_fields keys are snake_case (e.g. "arp_flooding")
        # while APIC returns camelCase (e.g. "arpFlood").  model_construct does
        # not resolve aliases, so we translate before calling it.
        # The alias map is cached per-class to avoid recomputing on every object.
        alias_map = target_cls._get_alias_map()
        raw_attrs = {alias_map.get(k, k): v for k, v in raw_attrs.items()}

        coerced: dict[str, Any] = {
            k: _coerce_apic_value(
                target_cls.model_fields[k].annotation if k in target_cls.model_fields else None,
                v,
            )
            for k, v in raw_attrs.items()
        }
        return target_cls, coerced, inner, aci_class

    @classmethod
    def from_apic(cls, data: dict[str, Any]) -> ManagedObject:
        """Deserialise an APIC JSON response envelope into a typed ManagedObject.

        Uses REGISTRY to dispatch to the correct subclass.  If the ACI class is
        not registered (not yet generated), falls back to the base ManagedObject.
        Children are recursively deserialised.

        Args:
            data: Dict in APIC envelope format, e.g.::

                {
                    "fvBD": {
                        "attributes": {"name": "web", "unicastRoute": "true"},
                        "children": [...]
                    }
                }

        Returns:
            Typed ManagedObject instance (or ManagedObject if class unknown).

        Note:
            **Deserialized objects start with a minimal ``model_fields_set``.**
            Only the naming props (e.g. ``name``) are pre-included; all other
            APIC attributes land in ``model_extra`` (absorbed via
            ``extra="allow"``).  Pydantic v2 automatically adds any field you
            assign directly to ``model_fields_set``, so a plain assignment is
            enough to include that field in the next ``to_apic()`` call::

                bd = session.get_mo("uni/tn-prod/BD-web", cls=fvBD)
                bd.unicast_routing = False   # ← Pydantic adds to model_fields_set
                bd.to_apic()                 # ← unicastRoute: "false" sent ✓

            For patch-style configuration, prefer a small design:
            ``tenant("prod").bd("web").set(unicast_routing=False).push(aci)``
            (see :mod:`niwaki.design`).

        Example::

            raw = {"fvBD": {"attributes": {"name": "web"}, "children": []}}
            bd = ManagedObject.from_apic(raw)
            isinstance(bd, fvBD)  # → True (if fvBD is registered)
        """
        # _fields_set is limited to naming props so that to_apic() on a
        # deserialized object remains surgical (only naming props are re-sent).
        target_cls, coerced, inner, aci_class = cls._dispatch_and_coerce(data)
        obj = target_cls.model_construct(
            _fields_set=set(target_cls._naming_props),
            **coerced,
        )
        # Retain the wire class name so a non-generated object can resolve its
        # readable field names through the catalogue (see ``__getattr__``).
        obj._wire_class = aci_class

        for child_raw in inner.get("children", []):
            obj.children.append(ManagedObject.from_apic(child_raw))

        return obj

    @classmethod
    def from_event(cls, data: dict[str, Any]) -> ManagedObject:
        """Deserialise one object-subscription push item into a typed ManagedObject.

        A sibling of :meth:`from_apic`, differing in exactly one respect:
        ``model_fields_set``. ``from_apic`` narrows it to the naming props,
        because a full read must stay surgical on round-trip through
        :meth:`to_apic`. A subscription push is a different shape entirely —
        confirmed live, a ``created`` event carries the full object, a
        ``modified`` event carries only the changed properties plus ``dn``,
        and a ``deleted`` event carries only ``dn`` — so reusing ``from_apic``'s
        naming-only narrowing here would be actively wrong: every field a
        modify delta did not mention would read as though it were a reported
        value instead of "this event said nothing about it," and the naming
        props themselves would be marked reported even when a modify/delete
        payload typically omits them.

        Instead, ``from_event`` sets ``model_fields_set`` to exactly the config
        fields *this event's payload* reported:
        ``set(coerced) & set(target_cls.model_fields)`` — mirroring the same
        "an instance whose ``model_fields_set`` faithfully describes what is
        populated" idiom :meth:`surgical` already uses for locally-built
        payloads, applied here to a payload read off the wire instead.
        ``event.mo.model_fields_set`` is therefore the caller's authoritative
        answer to "what did this event actually tell me?" — empty for a
        ``deleted`` event (identity only: ``dn`` lands in ``model_extra``,
        which is unaffected by ``model_fields_set``), the complete reported
        set for a ``created`` event.

        Args:
            data: One push item's envelope, matching :meth:`from_apic`'s shape
                but typically sparse, e.g.::

                    {"fvBD": {"attributes": {"dn": "uni/tn-prod/BD-web",
                                              "arpFlood": "yes",
                                              "status": "modified"}}}

        Returns:
            Typed ManagedObject instance (or the base ManagedObject for a
            class with no generated model), with ``model_fields_set`` matching
            exactly what the payload reported.

        Example::

            mo = ManagedObject.from_event(
                {"fvBD": {"attributes": {"dn": "uni/tn-prod/BD-web", "arpFlood": "yes"}}}
            )
            mo.model_fields_set   # → {"arp_flooding"} — "dn" is not a config field
            mo.arp_flooding       # → True
        """
        target_cls, coerced, inner, aci_class = cls._dispatch_and_coerce(data)
        fields_set = set(coerced) & set(target_cls.model_fields)
        obj = target_cls.model_construct(_fields_set=fields_set, **coerced)
        obj._wire_class = aci_class

        for child_raw in inner.get("children", []):
            obj.children.append(ManagedObject.from_event(child_raw))

        return obj


