"""ManagedObject — base class for all generated ACI primitives.

Every ACI class generated by the codegen inherits from ManagedObject.
The class itself is never instantiated directly.

Design notes:
- __init_subclass__ auto-registers each subclass in REGISTRY (no metaclass needed,
  avoids conflict with Pydantic's ModelMetaclass).
- model_fields_set is used by to_apic() to send only explicitly set fields, plus
  naming props — producing surgical POSTs instead of full object serialisation.
- extra="allow" absorbs APIC read-only props (modTs, uid, lcOwn …) from GET
  responses without raising validation errors.
- model_rebuild() is called at module level to resolve the self-referential
  children: list[ManagedObject] annotation.

Codegen contract — the ClassVars every generated subclass declares (they are
private: schema metadata for the SDK's own machinery, not a public API, and
deliberately kept out of the rendered class documentation):

- ``_aci_class``       ACI class name (``"fvBD"``).
- ``_rn_format``       RN format string (``"BD-{name}"``).
- ``_naming_props``    Property names forming the RN (``["name"]``).
- ``_secure_props``    Write-only props (passwords, pre-shared keys) — the APIC
                       never echoes them, so diff and plan must skip them.
- ``_config_issues``   The APIC's declared catalog of accepted-but-inconsistent
                       states (``{code: description}``, from ``configIssues``).
- ``_fault_codes``     Faults the APIC can raise on the class
                       (``{"F2305": "fltFvBDMulticastEnabledOnL2BD"}``).
- ``_relation_info``   Rs classes only: ``cardinality`` / ``enforceable`` /
                       ``resolvable``, from the schema's ``relationInfo``.
- ``_contains``        ACI class names valid as direct children.
- ``_mo_category``     APIC object category (``"Regular"``, ``"RelationshipToLocal"``).
- ``_write_access``    APIC privilege roles allowed to write the class.
- ``_is_observable``   The class supports WebSocket subscription.
- ``_is_faultable``    The APIC can raise faults on the class.
- ``_is_health_scorable`` The APIC tracks a health score for instances.
- ``_has_stats``       Statistics counters exist as children.
"""

from __future__ import annotations

from typing import Any, ClassVar, Self

from pydantic import BaseModel, ConfigDict

from niwaki.models._wire import from_wire, to_wire

REGISTRY: dict[str, type[ManagedObject]] = {}
"""ACI class name → generated Python class, for every imported model.

Populated by ``__init_subclass__`` as model modules are imported (lazily, on
first use), and used by ``from_apic`` to deserialise an APIC answer into the
right typed class.
"""

# Per-class alias map cache: avoids recomputing {aci_alias → python_field} on every from_apic().
_ALIAS_MAP_CACHE: dict[type[ManagedObject], dict[str, str]] = {}


def _coerce_apic_value(annotation: Any, value: Any) -> Any:
    """Coerce a raw APIC attribute onto the field's declared Python type.

    Thin seam over :func:`niwaki.models._wire.from_wire`: ``from_apic`` builds
    instances with ``model_construct``, so no validator ever runs and this is
    the only thing that gives a read-back field its declared type.

    Args:
        annotation: The Pydantic field annotation (possibly ``Annotated``).
        value: Raw value from the APIC attributes dict.

    Returns:
        The coerced value, or the original when the type keeps the wire form.
    """
    return from_wire(annotation, value)


class ManagedObject(BaseModel):
    """Base class for every ACI Managed Object.

    The 2,222 ACI classes are generated subclasses; this base provides the
    runtime behaviour they all share — RN computation, child containment
    validation, APIC serialisation (surgical: only the fields you set), and
    deserialisation back into the right typed class.

    Instances validate at construction: a wrong enum value, an out-of-range
    integer or a malformed IP never reaches the wire.

    Example::

        from niwaki.models.fv.fvBD import fvBD

        bd = fvBD(name="prod-bd", unicast_routing=True)
        bd.rn                 # → "BD-prod-bd"
        bd.to_apic()
        # → {"fvBD": {"attributes": {"name": "prod-bd", "unicastRoute": "true"}}}

    Every generated subclass also carries private ClassVars holding the APIC
    schema metadata (naming props, containment, fault and config-issue
    catalogs…) — they are the codegen contract, not a public API; see the
    module docstring for the full list.
    """

    model_config = ConfigDict(extra="allow", populate_by_name=True)

    _aci_class: ClassVar[str] = ""
    _rn_format: ClassVar[str] = ""
    _naming_props: ClassVar[list[str]] = []
    _secure_props: ClassVar[frozenset[str]] = frozenset()
    _config_issues: ClassVar[dict[str, str]] = {}
    _fault_codes: ClassVar[dict[str, str]] = {}
    _relation_info: ClassVar[dict[str, str | bool]] = {}
    _contains: ClassVar[frozenset[str]] = frozenset()
    _mo_category: ClassVar[str] = "Regular"
    _write_access: ClassVar[frozenset[str]] = frozenset()
    _is_observable: ClassVar[bool] = False
    _is_faultable: ClassVar[bool] = False
    _is_health_scorable: ClassVar[bool] = False
    _has_stats: ClassVar[bool] = False

    children: list[ManagedObject] = []

    @classmethod
    def _get_alias_map(cls) -> dict[str, str]:
        """Return a cached ``{aci_alias: python_field_name}`` mapping for this class.

        Built once per concrete class and stored in the module-level
        ``_ALIAS_MAP_CACHE`` so that repeated calls (e.g. bulk ``from_apic()``
        deserialisation) pay only a single dict lookup rather than a full
        comprehension over ``model_fields``.

        The wire name lives in ``validation_alias``/``serialization_alias``, not
        in ``alias``: the plain ``alias=`` is the one PEP 681 reads, and a type
        checker that reads it synthesises an ``__init__`` taking ``arpFlood`` and
        rejecting ``arp_flooding`` — the readable name would be an error in the
        user's editor while working at runtime.  The two-sided spelling keeps the
        wire name for pydantic and the Python name for the type checker.

        Returns:
            Dict mapping ACI camelCase attribute names to snake_case Python field
            names, for every field whose wire name differs from its Python name.
        """
        if cls not in _ALIAS_MAP_CACHE:
            _ALIAS_MAP_CACHE[cls] = {
                info.validation_alias: name
                for name, info in cls.model_fields.items()
                if isinstance(info.validation_alias, str)
            }
        return _ALIAS_MAP_CACHE[cls]

    def __init_subclass__(cls, **kwargs: Any) -> None:
        """Auto-register every concrete subclass in REGISTRY.

        Called automatically by Python when a class inherits from ManagedObject.
        Only registers when _aci_class is non-empty (concrete classes).

        Args:
            **kwargs: Forwarded to super().__init_subclass__.
        """
        super().__init_subclass__(**kwargs)
        if aci_class := getattr(cls, "_aci_class", None):
            REGISTRY[aci_class] = cls

    # ── DN / RN ───────────────────────────────────────────────────────────────

    @property
    def rn(self) -> str:
        """Compute the Relative Name from _rn_format and current naming prop values.

        Returns:
            RN string with all ``{prop}`` placeholders replaced.

        Example::

            bd = fvBD(name="web")
            bd.rn  # → "BD-web"
        """
        rn = self._rn_format
        for prop in self._naming_props:
            value = getattr(self, prop, "")
            wire = "" if value is None else to_wire(value)
            rn = rn.replace(f"{{{prop}}}", wire)
        return rn

    # ── Serialisation ─────────────────────────────────────────────────────────

    def to_apic(self) -> dict[str, Any]:
        """Serialise this object to the APIC JSON envelope format.

        Only fields that were explicitly set at construction time (tracked via
        ``model_fields_set``) are included in attributes, plus any naming props.
        This produces surgical POSTs — the APIC applies its own defaults for
        unset fields.

        Extra fields absorbed from APIC GET responses (``model_extra``) are never
        included in the serialised output.

        Returns:
            Dict in APIC envelope format::

                {
                    "fvBD": {
                        "attributes": {"name": "web", "unicastRoute": "true"},
                        "children": [...]   # only when children exist
                    }
                }

        Example::

            bd = fvBD(name="web", unicast_routing=True)
            bd.to_apic()
            # → {"fvBD": {"attributes": {"name": "web", "unicastRoute": "true"}}}
        """
        # Only send defined model fields (not APIC-originated extra fields)
        model_field_names = set(self.__class__.model_fields.keys()) - {"children"}
        fields_to_send = set(self._naming_props) | (self.model_fields_set & model_field_names)

        naming_set = set(self._naming_props)
        attrs: dict[str, Any] = {}
        for field_name in fields_to_send:
            val = getattr(self, field_name, None)
            if val is None:
                continue
            # Skip empty strings for optional fields — sending "" would silently
            # overwrite existing APIC values with an empty string. Naming props
            # are always included even when empty (the APIC requires them).
            if val == "" and field_name not in naming_set:
                continue
            # A renamed field carries the ACI attribute name in its
            # serialization_alias (arp_flooding → arpFlood, from_ → from).
            field_info = self.__class__.model_fields.get(field_name)
            aci_key = (
                field_info.serialization_alias
                if field_info and field_info.serialization_alias
                else field_name
            )
            attrs[aci_key] = to_wire(val)

        envelope: dict[str, Any] = {self._aci_class: {"attributes": attrs}}
        if self.children:
            envelope[self._aci_class]["children"] = [child.to_apic() for child in self.children]
        return envelope

    # ── Surgical construction ─────────────────────────────────────────────────

    @classmethod
    def surgical(cls, naming: dict[str, Any], **changes: Any) -> Self:
        """Construct a payload-only instance for surgical APIC writes.

        Builds an instance via ``model_construct`` with ``model_fields_set``
        limited to the naming props plus the explicitly provided *changes*.
        Calling ``to_apic()`` on the result sends only those fields — the APIC
        leaves all other attributes untouched (APIC upsert semantics).

        This is the canonical alternative to calling ``model_construct`` with
        ``_fields_set`` directly: it centralises the logic and prevents the
        common mistake of forgetting to include naming props in ``_fields_set``.

        Args:
            naming: Dict of naming prop values (e.g. ``{"name": "web"}``).
            **changes: Attribute values to include in the payload.

        Returns:
            A partially-populated instance ready for :meth:`to_apic`.

        Example::

            # Surgical update: only name + arp_flooding in the POST payload
            delta = fvBD.surgical({"name": "web"}, arp_flooding=True)
            delta.to_apic()
            # → {"fvBD": {"attributes": {"name": "web", "arpFlood": "true"}}}
        """
        return cls.model_construct(
            _fields_set=set(cls._naming_props) | set(changes.keys()),
            **{**naming, **changes},
        )

    # ── Deserialisation ───────────────────────────────────────────────────────

    @classmethod
    def from_apic(cls, data: dict[str, Any]) -> ManagedObject:
        """Deserialise an APIC JSON response envelope into a typed ManagedObject.

        Uses REGISTRY to dispatch to the correct subclass.  If the ACI class is
        not registered (not yet generated), falls back to the base ManagedObject.
        Children are recursively deserialised.

        Args:
            data: Dict in APIC envelope format, e.g.::

                {
                    "fvBD": {
                        "attributes": {"name": "web", "unicastRoute": "true"},
                        "children": [...]
                    }
                }

        Returns:
            Typed ManagedObject instance (or ManagedObject if class unknown).

        Note:
            **Deserialized objects start with a minimal ``model_fields_set``.**
            Only the naming props (e.g. ``name``) are pre-included; all other
            APIC attributes land in ``model_extra`` (absorbed via
            ``extra="allow"``).  Pydantic v2 automatically adds any field you
            assign directly to ``model_fields_set``, so a plain assignment is
            enough to include that field in the next ``to_apic()`` call::

                bd = session.get_mo("uni/tn-prod/BD-web", cls=fvBD)
                bd.unicast_routing = False   # ← Pydantic adds to model_fields_set
                bd.to_apic()                 # ← unicastRoute: "false" sent ✓

            For patch-style configuration, prefer a small design:
            ``tenant("prod").bd("web").set(unicast_routing=False).push(aci)``
            (see :mod:`niwaki.design`).

        Example::

            raw = {"fvBD": {"attributes": {"name": "web"}, "children": []}}
            bd = ManagedObject.from_apic(raw)
            isinstance(bd, fvBD)  # → True (if fvBD is registered)
        """
        aci_class = next(iter(data))
        target_cls: type[ManagedObject] = REGISTRY.get(aci_class, cls)
        inner = data[aci_class]

        # Use model_construct to bypass Pydantic validation:
        # - APIC returns sentinel strings like "not-applicable" for unset fields
        #   that don't match strict pattern validators (e.g. MAC address patterns).
        # - We trust the APIC to return well-formed data.
        # - _fields_set is limited to naming props so that to_apic() on a
        #   deserialized object remains surgical (only naming props are re-sent).
        # - Extra APIC fields (dn, modTs, uid, …) land in __pydantic_extra__
        #   because extra="allow" is set on the model config.
        # - _coerce_apic_value converts "yes"/"no" strings to Python bools (and
        #   digit-strings to int) so that mo_diff comparisons are type-correct.
        raw_attrs: dict[str, Any] = inner.get("attributes", {})

        # Remap ACI wire names → Python field names via Field aliases.
        # After D2 codegen, model_fields keys are snake_case (e.g. "arp_flooding")
        # while APIC returns camelCase (e.g. "arpFlood").  model_construct does
        # not resolve aliases, so we translate before calling it.
        # The alias map is cached per-class to avoid recomputing on every object.
        alias_map = target_cls._get_alias_map()
        raw_attrs = {alias_map.get(k, k): v for k, v in raw_attrs.items()}

        coerced: dict[str, Any] = {
            k: _coerce_apic_value(
                target_cls.model_fields[k].annotation if k in target_cls.model_fields else None,
                v,
            )
            for k, v in raw_attrs.items()
        }
        obj = target_cls.model_construct(
            _fields_set=set(target_cls._naming_props),
            **coerced,
        )

        for child_raw in inner.get("children", []):
            obj.children.append(ManagedObject.from_apic(child_raw))

        return obj


# Resolve the self-referential `children: list[ManagedObject]` annotation
ManagedObject.model_rebuild()
